    public function status($id)
    {
        $student = Student::find($id);
        // return $student;
        if($student->status == 1)
        {
            $student->status = 0;
            $student->save();
        }
        else
        {
            $student->status = 1;
            $student->save();
        }

        return back()->with('msg','Status Changed');

    }
